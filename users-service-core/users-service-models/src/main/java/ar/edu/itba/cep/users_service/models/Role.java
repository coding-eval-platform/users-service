/**
 * Enum containing the different roles of the platform.
 */
public enum Role {

    /**
     * Indicates that a {@link User} is a teacher.
     */
    TEACHER,

    /**
     * Indicates that a {@link User} is an admin of the platform.
     */
    ADMIN,
    ;
}
